import { createHash } from 'node:crypto';
import { mkdirSync, readFileSync, readdirSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = dirname(dirname(fileURLToPath(import.meta.url)));
const migrationsDir = join(root, 'supabase', 'migrations');
const outputFile = join(root, 'worker', 'src', 'generated', 'supabase-migrations.ts');

function normalizeSql(sql) {
  return sql
    .replace(/^\uFEFF/, '')
    .replace(/\r\n/g, '\n')
    .replace(/^\s*begin\s*;\s*/i, '')
    .replace(/\s*commit\s*;\s*$/i, '')
    .trim();
}

const migrations = readdirSync(migrationsDir)
  .filter((name) => name.endsWith('.sql'))
  .sort()
  .map((fileName) => {
    const version = fileName.replace(/\.sql$/i, '');
    const sql = normalizeSql(readFileSync(join(migrationsDir, fileName), 'utf8'));
    return {
      version,
      name: version.replace(/^\d+_?/, '') || version,
      checksum: createHash('sha256').update(sql).digest('hex'),
      sql,
    };
  });

mkdirSync(dirname(outputFile), { recursive: true });
writeFileSync(
  outputFile,
  `// Generated by scripts/generate-migrations.mjs. Do not edit by hand.\n\n` +
    `export type BundledMigration = {\n` +
    `  version: string;\n` +
    `  name: string;\n` +
    `  checksum: string;\n` +
    `  sql: string;\n` +
    `};\n\n` +
    `export const BUNDLED_SUPABASE_MIGRATIONS: BundledMigration[] = ${JSON.stringify(migrations, null, 2)};\n`,
);

console.log(`Generated ${migrations.length} Supabase migrations.`);
